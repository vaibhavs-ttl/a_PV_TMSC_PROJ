package com.ttl.model;

public class LabourSpareModel {

	
	private String desc="";
	private String type="";
	private String qty="";
	private String uom="";
	private String serviceType="";
	private String billingHours="";
	private String umrp="";
	private float value;
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getUom() {
		return uom;
	}
	public void setUom(String uom) {
		this.uom = uom;
	}
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getBillingHours() {
		return billingHours;
	}
	public void setBillingHours(String billingHours) {
		this.billingHours = billingHours;
	}
	public String getUmrp() {
		return umrp;
	}
	public void setUmrp(String umrp) {
		this.umrp = umrp;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
	
	
	
	
	
	
}
