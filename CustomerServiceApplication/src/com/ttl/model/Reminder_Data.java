package com.ttl.model;

public class Reminder_Data {

	public String rem_no;
	public String rem_vehicle_no;
	public String rem_date;
	public String userId;
	public int rem_id;
	public String getRem_no() {
		return rem_no;
	}

	public void setRem_no(String rem_no) {
		this.rem_no = rem_no;
	}

	public String getRem_vehicle_no() {
		return rem_vehicle_no;
	}

	public void setRem_vehicle_no(String rem_vehicle_no) {
		this.rem_vehicle_no = rem_vehicle_no;
	}

	public String getRem_date() {
		return rem_date;
	}

	public void setRem_date(String rem_date) {
		this.rem_date = rem_date;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getRem_id() {
		return rem_id;
	}

	public void setRem_id(int rem_id) {
		this.rem_id = rem_id;
	}
	
	

}
